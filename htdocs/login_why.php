<h1>Why log in?</h1>
<p>Logging in on this site has a (small but growing) number of advantages: at
the moment, registered users can:<ol>
<li>Bookmark a page<br/>
Your bookmarks will show up on the right-hand side of the site and will allow you to easily skip to the parts of the site you like most</li>
<li>Comment on a page<br/>
You will be able to leave a comment to the author of the page, telling him what you did or did not like about the page.</li>
</ol>
At some point in the future, other features will also be added.
</p>
<p>Because leaving your E-mail address is optional and no other personal information is gathered (you can look at the site's sources in CVS if you don't believe me) there are really no setbacks to using this feature. The feature's implementation uses a single cookie with your user ID which is used to retrieve your bookmarks from the database.
</p>
<h2>Why does this site offer this feature?</h2>
<p>There are two reasons why I (rlc) implemented this feature for this site: your bookmarking a page means you find that page interesting, which is something I'd like to know. Just counting the number of times a page is loaded doesn't actually give me much info (webbots can browse the internet but won't be too tempted to bookmark pages).</p>
<p>The other reason is that I think it makes it easier to access the site, which makes it easier for you to participate in this project, which is what this site
 is for.</p>
