<h1>Designing the language</h1>
<p>
The language design is discussed on the jail-ust-lang discussion group. That group will take the final decisions on any of the aspects of the language and is the final authority as far as language design is concerned. To be part of those decision-makers, you just have to subscribe to the discussion group by sending mail to jail-ust-lang-subscribe (at) lists (dot) sourceforge (dot) net.
</p>
<p>
No actual decisions have been taken yet, except that:
<ul>
<li>Jail is an object-oriented language</li>
<li>There are no non-object types in Jail</li>
<li>jail must be very simple to work with - the learning curve must be shallow enough for any non-developer to work with it</li>
</ul>
Though Jail means "Just Another <i>Interpreted</i> Language" it may well be a good idea to implement, say, a front-end to gcc for Jail, making it a compiled language. There is nothing that forbids that - as long as the requirements imposed by the jail-ust-lang list are met.
</p>
